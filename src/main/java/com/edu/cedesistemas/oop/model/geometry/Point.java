package com.edu.cedesistemas.oop.model.geometry;

public class Point extends Segment{
    private double x;
    private double y;

    public Point (double punto1,double punto2){
        x=punto1;
        y=punto2;
    }
    public double getX(){
        return getX();
    }
    public double getY(){
        return getY();
    }
    public double distancia(){
        return x+y;
    }

}
